const norm = (s: string) =>
  s.toLowerCase().trim().replace(/[.,!?]/g, '').replace(/ё/g, 'е');

export function getRandomVocabWord(excludeIds: string[] = []): VocabWord {
  const available = VOCAB_WORDS_RU.filter((w) => !excludeIds.includes(w.id));
  if (available.length === 0) {
    return VOCAB_WORDS_RU[Math.floor(Math.random() * VOCAB_WORDS_RU.length)];
  }
  return available[Math.floor(Math.random() * available.length)];
}

export function getWordsByDifficulty(difficulty: 1 | 2 | 3): VocabWord[] {
  return VOCAB_WORDS_RU.filter((w) => w.difficulty === difficulty);
}

export function checkSynonym(input: string, synonyms: string[]): boolean {
  const cleaned = norm(input);
  if (!cleaned) return false;
  return synonyms.some((s) => {
    const n = norm(s);
    return n === cleaned || cleaned.includes(n);
  });
}

export function findMatchedSynonym(
  input: string,
  synonyms: string[],
): string | null {
  const cleaned = norm(input);
  if (!cleaned) return null;
  const exact = synonyms.find((s) => norm(s) === cleaned);
  if (exact) return exact;
  return synonyms.find((s) => cleaned.includes(norm(s))) ?? null;
}

