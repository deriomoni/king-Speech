const normalize = (s: string): string =>
  s
    .toLowerCase()
    .trim()
    .replace(/ё/g, 'е')
    .replace(/[^а-яa-z\s]/g, '')
    .replace(/\s+/g, ' ');

const stem = (s: string): string => {
  const w = normalize(s);
  const trimmed = w
    .replace(
      /(ами|ями|ого|его|ому|ему|ой|ей|ую|юю|ые|ие|ых|их|ом|ем|ах|ях|ов|ев|ью|ам|ям|ться|тся|ешь|ишь|ает|яет|ует|ала|ало|али|ила|ило|или|ый|ий|ая|яя|ое|ее|ть|ил|ал|ит|ат|ят|ен|на|ны)$/,
      '',
    )
    .replace(/(ость|ение|ание|ство|тель|ница)$/, '');
  return trimmed || w;
};

const levenshtein = (a: string, b: string): number => {
  const m = a.length;
  const n = b.length;
  if (m === 0) return n;
  if (n === 0) return m;
  const dp: number[][] = Array.from({ length: m + 1 }, (_, i) =>
    Array.from({ length: n + 1 }, (_, j) => (i === 0 ? j : j === 0 ? i : 0)),
  );
  for (let i = 1; i <= m; i++) {
    for (let j = 1; j <= n; j++) {
      dp[i][j] =
        a[i - 1] === b[j - 1]
          ? dp[i - 1][j - 1]
          : 1 + Math.min(dp[i - 1][j], dp[i][j - 1], dp[i - 1][j - 1]);
    }
  }
  return dp[m][n];
};

export function checkSynonymAdvanced(
  input: string,
  synonyms: string[],
  foundAlready: string[],
): { matched: string | null; alreadyFound: boolean } {
  const normInput = normalize(input);
  if (!normInput) return { matched: null, alreadyFound: false };
  const stemInput = stem(input);

  for (const syn of synonyms) {
    const normSyn = normalize(syn);
    const stemSyn = stem(syn);

    let isMatch = false;
    if (normInput === normSyn) isMatch = true;
    else if (stemInput === stemSyn && stemInput.length >= 4) isMatch = true;
    else if (normInput.includes(normSyn) && normSyn.length >= 4) isMatch = true;
    else if (normSyn.length >= 6 && levenshtein(normInput, normSyn) === 1)
      isMatch = true;
    else if (stemSyn.length >= 4 && levenshtein(stemInput, stemSyn) <= 1)
      isMatch = true;

    if (isMatch) {
      const already = foundAlready.some(
        (f) => normalize(f) === normSyn,
      );
      if (already) return { matched: null, alreadyFound: true };
      return { matched: syn, alreadyFound: false };
    }
  }
  return { matched: null, alreadyFound: false };
}
