function generateAudience(): PersonDef[] {
  const people: PersonDef[] = [];
  const rows = [
    { y: 0, count: 10, headR: 7, gap: 28 },
    { y: 22, count: 11, gap: 27, headR: 8 },
    { y: 48, count: 12, gap: 26, headR: 9 },
    { y: 78, count: 10, gap: 30, headR: 11 },
    { y: 115, count: 8, gap: 36, headR: 14 },
  ];
  let idx = 0;
  for (const row of rows) {
    const totalW = row.count * row.gap;
    const startX = (SW - totalW) / 2 + 4;
    for (let i = 0; i < row.count; i++) {
      people.push({ x: startX + i * row.gap + (Math.random() * 6 - 3), y: row.y + (Math.random() * 5 - 2), headR: row.headR, bodyW: row.headR * 2.4, bodyH: row.headR * 1.8, toneIdx: idx++ });
    }
  }
  return people;
}

