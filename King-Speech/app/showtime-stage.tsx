// ── AUDIENCE PERSON ────────────────────────────────────────────────────────────
interface PersonDef {
  x: number;
  y: number;
  headR: number;
  bodyW: number;
  bodyH: number;
  toneIdx: number;
}

const HEAD_TONES = ["#1A2238", "#1E2A44", "#162030", "#1C2840", "#14203A", "#202840"];

function AudiencePerson({ person }: { person: PersonDef }) {
  const sway = useSharedValue(0);
  const bob = useSharedValue(0);
  const lean = useSharedValue(0);

  useEffect(() => {
    const delay = Math.random() * 2000;
    const interval = Math.random() * 3000 + 3000;
    sway.value = withDelay(delay, withRepeat(
      withTiming(Math.random() > 0.5 ? 2.5 : -2.5, { duration: interval }), -1, true
    ));
    const bobLoop = () => {
      const wait = Math.random() * 6000 + 4000;
      setTimeout(() => {
        bob.value = withSequence(withTiming(-4, { duration: 200 }), withTiming(0, { duration: 200 }));
        bobLoop();
      }, wait);
    };
    bobLoop();
    const leanLoop = () => {
      const wait = Math.random() * 10000 + 8000;
      setTimeout(() => {
        lean.value = withSequence(withTiming(Math.random() > 0.5 ? 8 : -8, { duration: 600 }), withTiming(0, { duration: 600 }));
        leanLoop();
      }, wait);
    };
    leanLoop();
    return () => { cancelAnimation(sway); cancelAnimation(bob); cancelAnimation(lean); };
  }, []);

  const style = useAnimatedStyle(() => ({
    transform: [{ rotate: `${sway.value}deg` }, { translateY: bob.value }, { translateX: lean.value }],
  }));

  const tone = HEAD_TONES[person.toneIdx % HEAD_TONES.length];
  return (
    <Animated.View style={[style, { position: "absolute", left: person.x, top: person.y, alignItems: "center" }]}>
      <View style={{ width: person.headR * 2, height: person.headR * 2, borderRadius: person.headR, backgroundColor: tone }} />
      <View style={{ width: person.bodyW, height: person.bodyH, backgroundColor: tone, borderTopLeftRadius: person.bodyW * 0.35, borderTopRightRadius: person.bodyW * 0.35, marginTop: 2 }} />
    </Animated.View>
  );
}

